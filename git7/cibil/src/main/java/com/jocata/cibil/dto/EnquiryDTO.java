package com.jocata.cibil.dto;

public class EnquiryDTO {
    private String institutionName;
    private String enquiryPurpose;
    private String enquiryDate;

    public String getInstitutionName() { return institutionName; }
    public void setInstitutionName(String institutionName) { this.institutionName = institutionName; }
    public String getEnquiryPurpose() { return enquiryPurpose; }
    public void setEnquiryPurpose(String enquiryPurpose) { this.enquiryPurpose = enquiryPurpose; }
    public String getEnquiryDate() { return enquiryDate; }
    public void setEnquiryDate(String enquiryDate) { this.enquiryDate = enquiryDate; }

}
